package com.speedy.skytrain;

public class TwitterDisplay {

	private int image;
	private String twitter_Info;
	
	public TwitterDisplay(int image, String twitter_Info) {
		super();
		this.image = image;
		this.twitter_Info = twitter_Info;
	}

	public int getImage() {
		return image;
	}

	public String getTwitter_Info() {
		return twitter_Info;
	}

	

}
